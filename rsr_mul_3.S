#include "common.i"

.global rsr_mul_3
.type rsr_mul_3, @function

// reduction 3
rsr_mul_3:
	PUSH_REGS
	MOVW R28, R24	// Y = R
	MOVW R30, R22	// Z = L
	// MOVW R26, R18	// X = M

	LDD R2, Y+24
	LDD R3, Y+25
	LDD R4, Y+26
	LDD R5, Y+27
	LDD R6, Y+28
	LDD R7, Y+29
	LDD R8, Y+30
	LDD R9, Y+31
	LDD R10, Y+32
	LDD R11, Y+33
	LDD R12, Y+34
	LDD R13, Y+35
	CLR R14	// CARRY 를 위한 레지스터 0으로 세팅
	CLR R15	// BURROW 를 위한 레지스터 0으로 세팅
	CLC		// CARRY FLAG 0으로 세팅

	LDD R0, Z+0		// 0,0,0,0,L1,...,L7,CARRY1
	ADD R6, R0
	LDD R0, Z+1
	ADC R7, R0
	LDD R0, Z+2
	ADC R8, R0
	LDD R0, Z+3
	ADC R9, R0
	LDD R0, Z+4
	ADC R10, R0
	LDD R0, Z+5
	ADC R11, R0
	LDD R0, Z+6
	ADC R12, R0
	LDD R0, Z+7
	ADC R13, R0
	ADC R14, R1

	LDD R0, Z+0		// 0,...,0,L0,L1,L2,L3,CARRY2
	ADD R10, R0
	LDD R0, Z+1
	ADC R11, R0
	LDD R0, Z+2
	ADC R12, R0
	LDD R0, Z+3
	ADC R13, R0
	ADC R14, R1

	MOVW R30, R20	// Z = H, H32,...,H43,CARRY3
	LDD R0, Z+32
	ADD R2, R0
	LDD R0, Z+33
	ADC R3, R0
	LDD R0, Z+34
	ADC R4, R0
	LDD R0, Z+35
	ADC R5, R0
	LDD R0, Z+36
	ADC R6, R0
	LDD R0, Z+37
	ADC R7, R0
	LDD R0, Z+38
	ADC R8, R0
	LDD R0, Z+39
	ADC R9, R0
	LDD R0, Z+40
	ADC R10, R0
	LDD R0, Z+41
	ADC R11, R0
	LDD R0, Z+42
	ADC R12, R0
	LDD R0, Z+43
	ADC R13, R0
	ADC R14, R1

	LDD R0, Z+36	// H36,...,H47,CARRY4
	ADD R2, R0
	LDD R0, Z+37
	ADC R3, R0
	LDD R0, Z+38
	ADC R4, R0
	LDD R0, Z+39
	ADC R5, R0
	LDD R0, Z+40
	ADC R6, R0
	LDD R0, Z+41
	ADC R7, R0
	LDD R0, Z+42
	ADC R8, R0
	LDD R0, Z+43
	ADC R9, R0
	LDD R0, Z+44
	ADC R10, R0
	LDD R0, Z+45
	ADC R11, R0
	LDD R0, Z+46
	ADC R12, R0
	LDD R0, Z+47
	ADC R13, R0
	ADC R14, R1

	LDD R0, Z+44	// H44,H45,H46,H47,0,...,0 BURROW1
	SUB R2, R0
	LDD R0, Z+45
	SBC R3, R0
	LDD R0, Z+46
	SBC R4, R0
	LDD R0, Z+47
	SBC R5, R0
	SBC R6, R1
	SBC R7, R1
	SBC R8, R1
	SBC R9, R1
	SBC R10, R1
	SBC R11, R1
	SBC R12, R1
	SBC R13, R1
	ADC R15, R1

	MOVW R30, R18	// M24,...,M35,BURROW2
	LDD R0, Z+24	
	SUB R2, R0
	LDD R0, Z+25
	SBC R3, R0
	LDD R0, Z+26
	SBC R4, R0
	LDD R0, Z+27
	SBC R5, R0
	LDD R0, Z+28
	SBC R6, R0
	LDD R0, Z+29
	SBC R7, R0
	LDD R0, Z+30
	SBC R8, R0
	LDD R0, Z+31
	SBC R9, R0
	LDD R0, Z+32
	SBC R10, R0
	LDD R0, Z+33
	SBC R11, R0
	LDD R0, Z+34
	SBC R12, R0
	LDD R0, Z+35
	SBC R13, R0
	ADC R15, R1

	LDD R0, Y+50	// R12~R23 CARRY 처리
	ADD R2, R0
	ADC R3, R1
	ADC R4, R1
	ADC R5, R1
	ADC R6, R1
	ADC R7, R1
	ADC R8, R1
	ADC R9, R1
	ADC R10, R1
	ADC R11, R1
	ADC R12, R1
	ADC R13, R1
	ADC R14, R1

	LDD R0, Y+51	// R12~R23 BURROW 처리
	SUB R2, R0
	SBC R3, R1
	SBC R4, R1
	SBC R5, R1
	SBC R6, R1
	SBC R7, R1
	SBC R8, R1
	SBC R9, R1
	SBC R10, R1
	SBC R11, R1
	SBC R12, R1
	SBC R13, R1
	ADC R15, R1

	// R14, R15에 있는 CARRY/BURROW 처리해주기
	STD Y+24, R2
	STD Y+25, R3
	STD Y+26, R4
	STD Y+27, R5
	STD Y+28, R6
	STD Y+29, R7
	STD Y+30, R8
	STD Y+31, R9
	STD Y+32, R10
	STD Y+33, R11
	STD Y+34, R12
	STD Y+35, R13
	LDD R0, Y+52	// R[52]에 있는 CARRY 가져오기
	ADD R14, R0
	STD Y+52, R14	// CARRY 저장
	LDD R0, Y+53	// R[53]에 있는 BURROW 가져오기
	ADD R15, R0
	STD Y+53, R15	// BURROW 저장

	POP_REGS
RET
